package com.stgson.security;

import com.google.common.collect.Sets;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;

import java.util.Set;
import java.util.stream.Collectors;

import static com.stgson.security.ApplicationUserPermission.*;

public enum ApplicationUserRole {
    DISTRIBUTEUR(Sets.newHashSet()),
    ADMIN(Sets.newHashSet(DEPOT_READ, DEPOT_WRITE, TRANSFERT_READ, TRANSFERT_WRITE)),
    VERIFICATEUR(Sets.newHashSet(DEPOT_READ, TRANSFERT_READ));

    private final Set<ApplicationUserPermission> permissions;

    ApplicationUserRole(Set<ApplicationUserPermission> permissions) {
        this.permissions = permissions;
    }

    public Set<ApplicationUserPermission> getPermissions() {
        return permissions;
    }

    public Set<SimpleGrantedAuthority> getSimpleGrantedAuthority() {
        Set<SimpleGrantedAuthority> permissions = getPermissions().stream()
                .map(permission -> new SimpleGrantedAuthority(permission.getPermission()))
                .collect(Collectors.toSet());
        permissions.add(new SimpleGrantedAuthority("ROLE_" + this.name()));
        return permissions;
    }
}